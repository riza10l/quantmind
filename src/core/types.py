"""
QuantMind Core Types
====================
Shared type definitions used across all modules. These are the fundamental
data structures that flow through the system.

Design Principle: Use Python dataclasses for lightweight value objects and
Pydantic models for validated configurations. Enums enforce type safety
for categorical values.
"""

from __future__ import annotations

import enum
from dataclasses import dataclass, field
from datetime import datetime
from typing import Any, Optional

import numpy as np
import pandas as pd


# ============================================================
# Enums
# ============================================================


class Timeframe(str, enum.Enum):
    """Supported OHLCV timeframes."""
    M1 = "1m"
    M5 = "5m"
    M15 = "15m"
    M30 = "30m"
    H1 = "1h"
    H4 = "4h"
    D1 = "1d"
    W1 = "1w"

    @property
    def minutes(self) -> int:
        """Return the timeframe duration in minutes."""
        mapping = {
            "1m": 1, "5m": 5, "15m": 15, "30m": 30,
            "1h": 60, "4h": 240, "1d": 1440, "1w": 10080,
        }
        return mapping[self.value]


class Side(str, enum.Enum):
    """Trade side."""
    BUY = "buy"
    SELL = "sell"


class OrderType(str, enum.Enum):
    """Order type."""
    MARKET = "market"
    LIMIT = "limit"
    STOP_LOSS = "stop_loss"
    TAKE_PROFIT = "take_profit"
    STOP_LIMIT = "stop_limit"


class OrderStatus(str, enum.Enum):
    """Order lifecycle status."""
    PENDING = "pending"
    SUBMITTED = "submitted"
