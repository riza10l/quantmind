@dataclass(frozen=True)
class OHLCV:
    """Single OHLCV bar."""
    timestamp: datetime
    symbol: str
    open: float
    high: float
    low: float
    close: float
    volume: float
    source: DataSource = DataSource.BINANCE
    timeframe: Timeframe = Timeframe.D1

    def to_dict(self) -> dict[str, Any]:
        return {
            "timestamp": self.timestamp,
            "symbol": self.symbol,
            "open": self.open,
            "high": self.high,
            "low": self.low,
            "close": self.close,
            "volume": self.volume,
            "source": self.source.value,
            "timeframe": self.timeframe.value,
        }


@dataclass
class Signal:
    """Trading signal generated by a model or strategy."""
    timestamp: datetime
    symbol: str
    signal_type: SignalType
    confidence: float  # 0.0 to 1.0
    model_name: str
    features_used: list[str] = field(default_factory=list)
    explanation: str = ""
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class Order:
    """An order to be submitted to the broker."""
    id: str
    timestamp: datetime
    symbol: str
    side: Side
    order_type: OrderType
    quantity: float
    price: Optional[float] = None  # None for market orders
    stop_price: Optional[float] = None
    status: OrderStatus = OrderStatus.PENDING
    filled_quantity: float = 0.0
    filled_price: float = 0.0
    commission: float = 0.0
    latency_ms: float = 0.0
    signal: Optional[Signal] = None  # The signal that triggered this order
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class Position:
    """Current position in a symbol."""
    symbol: str
    side: Side
    quantity: float
    entry_price: float
    current_price: float
    unrealized_pnl: float = 0.0
    realized_pnl: float = 0.0
    opened_at: Optional[datetime] = None

    @property
    def market_value(self) -> float:
        return self.quantity * self.current_price

    @property
    def cost_basis(self) -> float:
        return self.quantity * self.entry_price

    @property
    def return_pct(self) -> float:
        if self.entry_price == 0:
            return 0.0
        direction = 1.0 if self.side == Side.BUY else -1.0
        return direction * (self.current_price - self.entry_price) / self.entry_price


@dataclass
class Trade:
    """A completed trade (entry + exit)."""
    id: str
    symbol: str
    side: Side
    entry_price: float
    exit_price: float
    quantity: float
    entry_time: datetime
    exit_time: datetime
    pnl: float
    pnl_pct: float
    commission: float
    slippage: float
    holding_period_minutes: int
    signal: Optional[Signal] = None
    metadata: dict[str, Any] = field(default_factory=dict)


@dataclass
class PortfolioState:
    """Snapshot of the portfolio at a point in time."""
    timestamp: datetime
    equity: float
    cash: float
    positions: list[Position] = field(default_factory=list)
    open_orders: list[Order] = field(default_factory=list)
    total_pnl: float = 0.0
    total_commission: float = 0.0
    drawdown: float = 0.0
    max_drawdown: float = 0.0
    sharpe_ratio: float = 0.0
    sortino_ratio: float = 0.0

    @property
    def position_count(self) -> int:
        return len(self.positions)

    @property
    def total_exposure(self) -> float:
        return sum(abs(p.market_value) for p in self.positions)

    @property
    def leverage(self) -> float:
        if self.equity == 0:
            return 0.0
        return self.total_exposure / self.equity


